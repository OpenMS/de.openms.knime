package de.openms.knime.qchandling.qcidreader;

import org.knime.core.node.NodeDialogPane;
import org.knime.core.node.NodeFactory;
import org.knime.core.node.NodeView;

/**
 * <code>NodeFactory</code> for the "QCIDReader" Node. Reads ID tsv files from
 * QCExporter
 * 
 * @author Stephan Aiche
 */
public class QCIDReaderNodeFactory extends NodeFactory<QCIDReaderNodeModel> {

    /**
     * {@inheritDoc}
     */
    @Override
    public QCIDReaderNodeModel createNodeModel() {
        return new QCIDReaderNodeModel();
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int getNrNodeViews() {
        return 0;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeView<QCIDReaderNodeModel> createNodeView(final int viewIndex,
            final QCIDReaderNodeModel nodeModel) {
        return null;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean hasDialog() {
        return false;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public NodeDialogPane createNodeDialogPane() {
        return null;
    }

}
